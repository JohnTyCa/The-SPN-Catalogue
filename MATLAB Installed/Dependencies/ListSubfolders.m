% List subfolders in directory.
% 
% ======================================================================= %
% Required Inputs:
% ======================================================================= %
%
% folder    -   Main directory to look for subfolders.
%
% ======================================================================= %
% Optional Inputs:
% ======================================================================= %
%
% 
% 
% ======================================================================= %
% Outputs:
% ======================================================================= %
%
% subFolds  -   Subfolders in main folder.
% 
% ======================================================================= %
% Example
% ======================================================================= %
%
% subFolds = ListSubfolders('D:\mainFolder\');
% 
% ======================================================================= %
% Dependencies.
% ======================================================================= %
% 
% 
% 
% ======================================================================= %
% UPDATE HISTORY:
%
% 15/10/2018 (v1.0) -   V1.0 Created.
%
% ======================================================================= %

function subFolds = ListSubfolders(folder)
d = dir(folder);
isub = [d(:).isdir];
subFolds = {d(isub).name}';
subFolds(ismember(subFolds,{'.','..'})) = [];
end